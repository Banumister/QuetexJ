/**
 * QuetexJ is a Java Swing MVC sets library
 * that supports pseudo endless text-component.
 *
 * <ul>
 * <li>QuetexJ is useful for outputting adhoc text information
 * like logging.
 * <li>QuetexJ automatically chops beginning of text to free memory
 * according to height of text component behind view-port.
 * Of course the physical line layouts are kept.
 * <li>QuetexJ works closely with vertical JScrollBar
 * to keep view-port view when chopping text.
 * <li>QuetexJ supports auto-tracking mode
 * that always displays last part of text component to view-port.
 * <li>QuetexJ includes log handler that complies with java.util.logging.
 * <li>QuetexJ archive includes test-harness GUI for trial use.
 * </ul>
 *
 * @see <a href="https://github.com/olyutorskii/QuetexJ">
 * https://github.com/olyutorskii/QuetexJ
 * </a>
 */

